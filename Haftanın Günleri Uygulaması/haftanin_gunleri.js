let sayi = prompt("Lütfen 1 ile 7 arasinda bir sayi seciniz");


switch(sayi){
    case "1":
        console.log("Pazartesi");
        break;
    case "2":
        console.log("Sali");
        break;
    case "3":
        console.log("Çarşamba");
        break;
    case "4":
        console.log("Perşembe");
        break;
    case "5":
        console.log("Cuma");
        break;
    case "6":
        console.log("Cumartesi");
        break;
    case "7":
        console.log("Pazar");

    default:
        console.log("Lütfen geçerli bir sayi giriniz!");
}


